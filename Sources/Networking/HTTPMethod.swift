/**
 Enum type representing possible HTTP methods.
 */
public enum HTTPMethod: String {
    
    /// `GET` method.
    case get = "GET"
    /// `PUT` method.
    case put = "PUT"
    /// `POST` method.
    case post = "POST"
    /// `DELETE` method.
    case delete = "DELETE"
    /// `PATCH` method.
    case path = "PATCH"
    
}
